                  <br />
                  <br />
                  <span style="color:#a31e5e"><strong>Rates can start as low as $14 per hour per family</strong></span> depending on the nanny you choose to employ.
                  <br />
                  <br />
                  <span style="color:#0099da"><strong>Care can take place</strong></span> at the <span style="color:#0099da"><strong>agreed home, or alternate</strong></span> between the two.
                  <br />
                  <br />
                  <span style="color:#a31e5e"><strong>Shared care can be split 50/50</strong></span> or one family may only require shorter hours. This is agreed upon in the interview and written into the contract.
                  <br />
                  <br />
                  Many nannies are prepared to help with <span style="color:#0099da"><strong>cooking meals</strong></span> and <span style="color:#0099da"><strong>children's washing.</strong></span>
