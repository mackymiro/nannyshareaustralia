                            </a>
                    </div>
                    <div id="collapsefourteen1" class="panel-collapse collapse">
                        <div class="panel-body">
                         Yes. When hiring a nanny you are binding into a business agreement and it is required that you set up a PAYG system to cover any tax obligations. For more information visit  <a href="http://www.ato.gov.au" title="www.ato.gov.au">www.ato.gov.au</a> or call ATO on 132 478.  You may prefer to have a payroll service handle the nannies salary.
                          Please visit <a href="http://www.domesticpayroll.co.au" title="www.domesticpayroll.com.au">domesticpayroll.com.au</a> If your nanny has an ABN she will be self-employed and responsible for paying her own taxes.    
